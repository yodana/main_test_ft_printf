int		ft_isuppercase(char *str)
{
	int i;

	i = 0;
	if (str)
	{
		while (str[i])
		{
			if (str[i] <= 64 || str[i] >= 91)
				return (0);
			i++;
		}
		return (1);
	}
	return (0);
}
